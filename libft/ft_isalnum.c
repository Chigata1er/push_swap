#include <unistd.h>
#include "./libft.h"

int	ft_isalnum(int c)
{
	if ((c >= '0' && c <= '9') || (c <= 'Z' && c >= 'A')
		|| (c <= 'z' && c >= 'a'))
		return (1);
	return (0);
}
